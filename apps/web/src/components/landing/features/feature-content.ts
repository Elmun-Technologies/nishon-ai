import type { FeatureIconKey } from './feature-icons'

export type CategoryId = 'execution' | 'aiOpt' | 'analytics' | 'governance'

export interface FeatureStep {
  title: string
  desc: string
}

export interface FeatureFaq {
  q: string
  a: string
}

export interface ProblemHook {
  headline: string
  pain: string[]
  cost: string
}

export interface ValueProp {
  title: string
  body: string
  highlight?: string
}

export interface ComparisonRow {
  manual: string
  adspectr: string
}

export interface UseCase {
  persona: string
  role: string
  scenario: string
  outcome: string
}

export interface MetricItem {
  value: string
  label: string
  context: string
}

export interface FeatureContent {
  slug: string
  category: CategoryId
  iconKey: FeatureIconKey
  hero: {
    eyebrow: string
    title: string
    description: string
  }
  /** Long-tail SEO keywords surfaced in metadata + naturally throughout copy. */
  keywords: string[]
  mockHeader: string
  bullets: { title: string; desc: string }[]
  steps: FeatureStep[]
  faq: FeatureFaq[]
  related: string[]
  cta?: { label: string; href: string }
  /** Short SEO description (~155 chars). */
  seoDescription: string
  /** "Знакомо?" — problem statement above value props. */
  problemHook?: ProblemHook
  /** Detailed selling points — bigger than bullets. */
  valueProps?: ValueProp[]
  /** Side-by-side: without AdSpectr vs with AdSpectr. */
  comparison?: { title: string; description?: string; rows: ComparisonRow[] }
  /** Persona-based scenarios. */
  useCases?: { title: string; description?: string; cases: UseCase[] }
  /** Outcome stats showcase. */
  metrics?: { title: string; description?: string; items: MetricItem[] }
}

