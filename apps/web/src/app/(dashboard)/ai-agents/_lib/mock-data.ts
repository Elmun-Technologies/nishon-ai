import type { PlatformAgentRole, VerticalId } from '@/lib/ai-agents/types'

export type AgentStatus = 'active' | 'paused' | 'testing' | 'draft'
export type AgentAction = 'budget_increase' | 'budget_decrease' | 'pause_ad' | 'launch_ad' | 'reallocate' | 'creative_swap'
export type ApprovalStatus = 'pending' | 'approved' | 'rejected' | 'auto_approved'

export interface AgentRecommendation {
  id: string
  agentId: string
  agentName: string
  action: AgentAction
  title: string
  rationale: string
  confidence: number // 0-1
  estimatedImpactUsd: number
  campaignName: string
  createdAt: string // ISO
  approvalStatus: ApprovalStatus
}

export interface MyAgent {
  id: string
  name: string
  role: PlatformAgentRole | 'custom'
  vertical: VerticalId
  status: AgentStatus
  source: 'platform' | 'rented' | 'self_made'
  /** ISO timestamp when this agent was rented/created. */
  startedAt: string
  /** Total recommendations made. */
  recommendationsCount: number
  /** Number of approved recommendations. */
  approvedCount: number
  /** Estimated savings/profit in USD generated by this agent's actions. */
  impactUsd: number
  /** Whether this agent has auto-approval enabled (high-confidence only). */
  autoApprove: boolean
  /** Monthly cost (rented only). */
  monthlyCostUsd?: number
  /** Avatar emoji */
  emoji: string
  /** Brand color */
  accent: string
  /** Recent recommendations (preview). */
  recent: AgentRecommendation[]
}

export interface StoreAgent {
  id: string
  name: string
  author: string
  authorAvatar: string
  vertical: VerticalId
  description: string
  priceMonthlyUsd: number
  pricePerActionUsd?: number
  status: 'testing' | 'published'
  testDaysRemaining?: number
  /** Public stats */
  rentersCount: number
  avgRoasMultiplier: number
  rating: number // 1-5
  reviewsCount: number
  tags: string[]
  emoji: string
  accent: string
}

