/** All pending recommendations across active agents — for runtime dashboard. */
export function getPendingRecommendations(): AgentRecommendation[] {
  return DEMO_MY_AGENTS.flatMap((a) => a.recent).filter(
    (r) => r.approvalStatus === 'pending',
  )
}

/** Cumulative impact across all agents. */
export function getTotalImpactUsd(): number {
  return DEMO_MY_AGENTS.reduce((sum, a) => sum + a.impactUsd, 0)
}

/** Total approval rate. */
export function getApprovalRate(): number {
  const total = DEMO_MY_AGENTS.reduce((s, a) => s + a.recommendationsCount, 0)
  const approved = DEMO_MY_AGENTS.reduce((s, a) => s + a.approvedCount, 0)
  if (total === 0) return 0
  return Math.round((approved / total) * 100)
}

export function formatRelativeTime(iso: string): string {
  const diff = Date.now() - new Date(iso).getTime()
  const min = Math.floor(diff / 60_000)
  if (min < 1) return 'hozirgina'
  if (min < 60) return `${min} daqiqa oldin`
  const hr = Math.floor(min / 60)
  if (hr < 24) return `${hr} soat oldin`
  const days = Math.floor(hr / 24)
  if (days < 7) return `${days} kun oldin`
  return new Date(iso).toLocaleDateString('uz-UZ')
}
